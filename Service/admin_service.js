exports.verifyEmail = async (token) => {
    const admin = await Admin.findOne({
        verifyToken: token,
        verifyTokenExpires: { $gt: Date.now() },
    });

    if (!admin) {
        throw new Error("Invalid or expired token");
    }

    admin.verifyToken = null;
    admin.verifyTokenExpires = null;
    admin.verified = true;
    await admin.save();

    return admin;
};

